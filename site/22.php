      <h2>22 Motivos para adotar</h2>
      <h3> 1 - Menos abandono</h3>
<p>Mesmo com o intenso trabalho de diversas ONGs e sociedades protetoras, em São Paulo todos os dias são abandonados mais de 60 animais só no Centro de Zoonoses. Adotando um animal que foi deixado na rua, você estará ajudando a diminuir esse triste número que cresce cada dia mais.
      </p>
      <h3>2 - Mudança na vida do peludo</h3>
      <p>
Imagina pegar um cachorro ou gato em um abrigo e dar pra ele o carinho e todos os cuidados que ele precisa? Qual animal não merece isso? Além disso, a sensação de bem estar de ter contribuído para vida deste animal não tem preço.</p>
<h3>3 - Diminiu o estresse</h3>
<p>Você vai ficar muito menos estressado com o trabalho na companhia de um animalzinho! Uma pesquisa feita pela Universidade da Comunidade da Virginia mediu o nível de estresse de funcionários de uma empresa, durante uma semana. Uma parte destes tiveram que levar seus cachorros para o trabalho e a outra seguiria a rotina normalmente, sem companhia dos bichinhos. Todos os voluntários tiveram de responder a questionários e ceder amostras da própria saliva aos pesquisadores. No final do dia, os funcionários “solitários” acabavam o dia mais estressado do que os felizardos acompanhados pelo cão.</p>
<div class="fakeimg2" align="center"><img src="img1.jpg"></div> 
<h3> 4 - Vaga para outros cães  </h3>
<p> As ONGs costumam estar lotadas, transbordando de cães. Quando você adota um animal em algum dessas instituições, está abrindo uma vaga para outro que precisa de ajuda.  </p>
<h3> 5 - Grande amigo  </h3>
<p> Sem dúvida nenhuma, ele será um bom companheiro e guardião. Ficará incansavelmente ao seu lado e, caso necessite desabafar, ele estará ali, ouvindo atentamente, sendo um fiel amigo. Além disso, adotar também é um ato de amor e solidariedade.  </p>
<h3>  6 - Eles compreendem a dor humana </h3>
<p> Sabe quando você está pra baixo e gostaria de uma companhia? A Universidade Goldsmiths divulgou um estudo que mostra que cães estão mais propensos a se aproximarem de pessoas tristes ou que estão passando por algum sofrimento. Isso mostra que cães têm empatia por pessoas e querem ajudar aqueles que estão com dor.  </p>
<h3> 7 - Voltando a ser criança  </h3>
<p> O seu espírito de criança será despertado quando ele pedir pra brincar com você.  </p>
<h3>  8 - Ajuda você a ser mais sociável </h3>
<p> Além de ser benéfico a saúde, cachorros contribuem para sua vida ser mais sociável.
O Periódico Britânico de Medicina concluiu que cães agem como catalisadores sociais, que ajudam pessoas a saírem de casa mais vezes, a abordarem desconhecidos facilmente e ainda reduz a solidão.</p>
<h3>  9 - Amor igual </h3>
<p>  O cão de raça ama da mesma forma que um vira-lata que está na rua ama. </p>
<h3> 10 - Vive intensamente  </h3>
<p> Você já parou pra pensar como um cão vive? O cão não remói o passado nem se preocupa com o futuro. Ele vive intensamente cada momento, aproveitando a comida e se deliciando com uma longa brincadeira, tira uma boa soneca à tarde e para eles dar uma volta no quarteirão é a melhor coisa do mundo. Viva como um cão, e você terá uma vida maravilhosa repleta de felicidade e bons momentos.  </p>
