      <h3>Caso do Carrefour</h3>
      <h5>Segundo o site Exame...</h5>
      <p>Segundo denúncia, animal teria sido morto por um segurança do supermercado; Carrefour afasta equipe para investigação</p>

      <div class="fakeimg2" align="center"><img src="cachorro-osasco-carrefour.png"></div> 
      <p>São Paulo – A rede de supermercados Carrefour está no centro de uma polêmica. Na última quarta-feira (28), um segurança do supermercado teria espancado um cachorro na unidade de Osasco da rede, em São Paulo.</p>

      <p>A denúncia feita nas redes sociais mostra imagens do animal com as patas traseiras feridas e marcas de sangue no chão da loja. Defensores dos animais e ativistas dizem ainda que houve tentativa de envenenamento do animal.</p>

      <p>O cachorro chegou a ser socorrido pelo Centro de Controle de Zoonoses (CCZ) mas não resistiu ao ferimento e morreu. Uma versão de atropelamento chegou a ser levantada, mas logo foi contestada por outros funcionários que teriam testemunhado o ocorrido e dito que o animal teria sido agredido a pauladas.</p>

      <p>Um laudo determinará a causa da morte. O cachorro estava na loja de Osasco havia alguns dias e chegou a receber água e alimentos dos funcionários do local. Ainda de acordo com denúncias nas redes sociais, o segurança da loja teria agredido o cachorro após uma suposta ordem de seu superior para “limpar” o mercado, que receberia naquele dia visita de executivos da empresa.</p>

      <p>Atuante na causa animal, o delegado Bruno Lima esteve no local e afirmou que um inquérito vai apurar se o cachorro também foi envenenado e se o segurança que o matou cumpria ordens da chefia ou agiu por conta própria.</p>

      <p>"Estaremos acompanhando de perto até que esse crime seja solucionado. Temos algumas testemunhas que confirmam o ato cruel e que identificaram o autor do crime. Infelizmente a dor que o animal sofreu não temos como apagar e também a sua vida trazer de volta, mas seremos sua voz e lutaremos em seu nome”, declarou o delegado.</p>

      <p>No mesmo dia, um grupo de ativistas e protetores de animais protestou na unidade. As contas oficiais do Carrefour no Instagram e Facebook também têm recebido centenas de mensagens e críticas repudiando o ocorrido. </p>

      <p>O artigo 32 da Lei de Crimes Ambientais (nº 9.605/98) considera crime as práticas de abuso, ferir ou mutilar animais domésticos, silvestres, nativos ou exóticos e que podem render pena de detenção de três meses a um ano, além de multa.</p>

      <h4>Outro lado</h4>

      <p>O  Carrefour emitiu um comunicado sobre o caso, afirmando que repudia maus-tratos contra animais e que afastou temporariamente a equipe de segurança que estava de plantão naquele dia enquanto prosseguem as investigações. Leia na íntegra abaixo:</p>

      <p>“A rede informa que repudia veementemente qualquer tipo de maus-tratos. Esclarece ainda que, preventivamente, afastou a equipe responsável pela segurança do local no dia da ocorrência até que a rigorosa apuração em curso seja concluída e as devidas providências adotadas. Reforça também que, assim que notou a presença do animal nas dependências da loja, o acolheu, oferecendo água e comida, até que a equipe do Centro de Controle de Zoonoses de Osasco chegasse ao local para o devido atendimento.”</p>

      <h3>Porque você está salvando uma vida</h3>
      <p>A cada ano milhões de animais nascem já nas ruas ou acabam sendo abandonados. Os abrigos e organizações de cuidados com animais não dão conta da demanda. Se mais pessoas adotassem ao invés de comprar, esse número cairia de forma considerável. Um animal adotado em um abrigo deixa a vaga para outro ser resgatado.</p>
      <div class="fakeimg2" align="center"><img src="cgs.jpg"> </div> 
      <h3>Porque você vai ter um animal excelente</h3>
      <p>Cães e gatos resgatados em abrigados muitas vezes passam por exames, vacinas e castração. Muitos animais estão nesses locais não porque fizeram algo errado.

      Muitos apenas “sobraram” depois de um divórcio ou uma mudança. Ou seja, já são treinados e acostumados a viver com uma família.</p>
      <p>Além disso, Os cães popularmente conhecidos como vira-latas possuem algumas vantagens e peculiaridades. Por ser uma mistura de raças, o termo correto é SRD (sem raça definida) e cada pet destes é único, devido a sua combinação de genes e mistura de raças ser totalmente exclusiva. “Não existe um padrão. O vira-lata é a ausência de raça pura, e a origem pode ser qualquer mistura, entre qualquer raça ou entre outros vira-latas. Cada um carrega uma carga genética única”, explica a veterinária Mireille Sabbagh.</p>
      <p>Justamente por ser um cão que não tenha uma carga genética exclusiva, o vira-lata é o resultado de uma seleção natural, por onde podem ter passado várias raças no passado. Os cães sem raça definida se tornam mais resistentes a alguns tipos de doenças. “Justamente pela mistura o vira-lata é mais resistente. Quando cruzamos cães da mesma raça, selecionamos não somente as características genéticas externas, vai junto desta seleção os genes das doenças que mais acometem àquela raça em questão. Quando misturamos as raças portanto, a mistura de genes torna este animal menos propenso a desenvolver doenças de origem genética”, argumenta Mireille.</p>

